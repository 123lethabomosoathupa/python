# Define a dictionary with user information
user_0 = {
    'username': 'efermi',
    'first': 'enrico',
    'last': 'fermi',
}

# Loop through all key-value pairs in the dictionary
for key, value in user_0.items():
    # Print the key
    print(f"\nKey: {key}")
    # Print the value associated with the key
    print(f"Value: {value}")
